fun main(args: Array<String>) {

    var num1 = 69F
    var num2 = 23F
    var operation: Float

    operation = num1 + num2
    println("The sum is :"+operation)

    operation = num1 - num2
    println("The difference is :"+operation)

    operation = num1 * num2
    println("The product is:"+operation)

    operation = num1 / num2
    println("The quotient is :"+operation)

    operation = num1 % num2
    println("Modulus is :"+operation)
}